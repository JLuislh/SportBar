    private void PromoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PromoMouseClicked
       DecimalFormat df = new DecimalFormat("#.00");
        try {
            InsertarProducto c = BDIngresos.BuscarProductoPromo(Integer.parseInt(String.valueOf(Promo.getModel().getValueAt(Promo.getSelectedRow(),0))));
            CODIGO.setText(String.valueOf(c.getCodigo()));
            DESCRIPCION.setText(c.getDescripcion());
            PRECIO.setText(String.valueOf(df.format(c.getPrecio())));
            if(c.getStatus() == 1){PROM.setSelected(true);}else{PROM.setSelected(false);}
            
          
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error mas"+e);
        } 
        BEditar.setEnabled(true);
        
        
    }//GEN-LAST:event_PromoMouseClicked

    private void BGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BGuardarActionPerformed
       
        if(PRECIO.getText().compareTo("")!=0 && DESCRIPCION.getText().compareTo("")!=0 ){
        try {
            InsertarProducto t = new InsertarProducto();
            
            t.setCodigo(Integer.parseInt(CODIGO.getText()));
            t.setDescripcion(DESCRIPCION.getText());
            t.setPrecio(Double.parseDouble(PRECIO.getText()));
            if(PROM.isSelected()){t.setStatus(1);}else{t.setStatus(2);}
            BDIngresos.UpdatePromo(t);
            JOptionPane.showMessageDialog(null, "PROMO ACTUALIZADA...");
            limpiar();
            
        } catch (Exception e){
            System.err.println("error"+e);
        }
        }else{JOptionPane.showMessageDialog(null, "Llene todos Los campos..");}
        
     
    }//GEN-LAST:event_BGuardarActionPerformed

