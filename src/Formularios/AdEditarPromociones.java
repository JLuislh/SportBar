      private void ListarProductosPedidos(){
     
        ArrayList<InsertarProducto> result = BDIngresos.BDIngresosPromo();
        RecargarTabla(result);  
    }
     private void RecargarTabla(ArrayList<InsertarProducto> list) {
         DecimalFormat df = new DecimalFormat("#.00");
              Object[][] datos = new Object[list.size()][3];
              int i = 0;
              for(InsertarProducto t : list)
              {
                  datos[i][0] = t.getCodigo();
                  datos[i][1] = t.getDescripcion();
                  datos[i][2] = df.format(t.getPrecio());
                  i++;
              }    
             Promo.setModel(new javax.swing.table.DefaultTableModel(
                datos,
                new String[]{
                "CODIGO INVENTARIO","DESCRIPCION","PRECIO"
             })
             {  
                 @Override
                 public boolean isCellEditable(int row, int column){
                 return false;

             }
             });
             Promo.getColumnModel().getColumn(1).setCellRenderer(new TextAreaRenderer());
             TableColumn columna1 = Promo.getColumn("CODIGO INVENTARIO");
             columna1.setPreferredWidth(-20);
             TableColumn columna2 = Promo.getColumn("DESCRIPCION");
             columna2.setPreferredWidth(275);
             TableColumn columna3 = Promo.getColumn("PRECIO");
             columna3.setPreferredWidth(35);
             
     }
    
    

